public class HealthWorker {


    // Declaratiion of variables

    String fName;
    String lName;
    double mSalary;
    String HWID;
    double incriment;
    double ySalary;


    // This is the constructor of the class HealthWorker

    public HealthWorker (String Name){
        this.fName = Name;

    }
    // Assign the last name of the HealthWorker  to the variable lName
    public void lastName (String lastName){
        lName = lastName;

    }
    // WorkID of the HealthWorker to the variable HWID
    public void HWorkID (String HWorkID){
        HWID = HWorkID;

    }
    // MonthSalary of the HealthWorker to the variable mSalary
    public double MonthSalary ( double MonthSalary){
        mSalary = MonthSalary;
        ySalary = mSalary * 12;
        return mSalary;

    }
    // Method to retrive the increment from an object
    public int getincriment (){
        incriment = ySalary * 1.10;
        return (int) incriment;
    }
    /* Print the HealthWorker details */
    public void printHealthWorker(){
        System.out.println("Name: " + fName);
        System.out.println("Surname: " + lName);
        System.out.println("Worker ID: " + HWID);
        System.out.println("Salary: " + ySalary);

    }
}
